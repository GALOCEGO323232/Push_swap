#include "push_swap.h"

void	pa(t_node **a, t_node **b)
{
	t_node		*node;

	if (!*b)
		return ;
	node = *b;
	*b = (*b)->next;
	if (*b)
		(*b)->prev = NULL;
	node->next = *a;
	if (*a)
		(*a)->prev = node;
	*a = node;
	node->prev = NULL;
	ft_printf("pa\n");
}
